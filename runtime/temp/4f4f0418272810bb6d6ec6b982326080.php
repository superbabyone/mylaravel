  layui.use(['layer'],function(){
    var layer = layui.layer;

    //内容改变
    $('.changearea').change(function(){
      var _this = $(this);
      _this.nextAll('select').html('<option>--请选择--</option>');
      var id = _this.val();
      $.ajax({
        url:"<?php echo url('getArea'); ?>",
        type:'post',
        data:{id:id},
        dataType:'json',
        success:function(res){
          var _option = "<option>--请选择--</option>";
          for(var i=0;i<res.length;i++){
            _option += "<option value='"+res[i]['id']+"'>"+res[i]['name']+"</option>"
          }
          _this.next('select').html(_option);
        },
      });
    })
    //点击添加
    $('#add').click(function(){
      var _this = $(this);
      var data = {};
      data.province = $('#province').val();
      data.city = $('#city').val();
      data.area = $('#area').val();
      data.address_name = $('#address_name').val();
      data.address_tel = $('#address_tel').val();
      data.address_detail = $('#address_detail').val();
      data.address_mail = $('#address_mail').val();
      data.is_default = $('#is_default').prop('checked');
      if(data.is_default == true){
        data.is_default = 1
      }else{
        data.is_default = 2
      }
      console.log(data);
      $.ajax({
        url:"<?php echo url('addHandle'); ?>",
        type:'post',
        data:data,
        dataType:'json',
        success:function(res){
          if(res.code == 1){
            layer.msg(res.font,{icon:6},function(){
              location.href="addressAdd";
            });
          }else{
            layer.msg(res.font,{icon:res.code});
          }
        }
      })
    })
    //点击设为默认
    $('.update').click(function(){
      var _this = $(this);
      var address_id = _this.parent('p').siblings("table[class=add_t]").attr('address_id');
      $.ajax({
        url:'<?php echo url("update"); ?>',
        type:'post',
        data:{address_id:address_id},
        dataType:'json',
        success:function(res){
          if(res.code == 1){
            layer.msg(res.font,{icon:6},function(){
              location.href="addressAdd";
            });
          }else{
            layer.msg(res.font,{icon:5});
          }
        },
      });
    })
    //点击编辑
    $('.edit').click(function(){
      var _this = $(this);
      var address_id = _this.parent('p').siblings("table[class=add_t]").attr('address_id');
      window.location.href="<?php echo url('Address/edit'); ?>?address_id="+address_id;
    })
    //点击叉号
